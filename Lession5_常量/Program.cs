namespace Lession5_常量
{
    internal class Program
    {
        static void Main(string[] args)
        {
            Console.WriteLine("常量const");

            #region 声明
            //关键词cont
            //const 变量类型 变量名 = 初始值；
            //常量必须初始化
            //不能被修改
            const int myAtk = 5;
            //myAtk = 10; 不能修改常量
            #endregion
        }
    }
}
